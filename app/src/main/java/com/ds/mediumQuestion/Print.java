package com.ds.mediumQuestion;


import java.util.List;

class Print {

    public static void Println(int[] ar){
        for (int a : ar){
            System.out.print(a+", ");
        }
    }

    public static void PrintList(List<Integer> ar){
        for (int a : ar){
            System.out.print(a+", ");
        }
    }
}
